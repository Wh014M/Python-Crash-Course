age_0 = 21

if age_0 < 2:
	print("You are a baby!")

elif age_0 >= 2 and age_0 < 4:
	print("You are a toddler!")

elif age_0 >= 4 and age_0 < 13:
		print("You are a kid!")

elif age_0 >= 13 and age_0 < 20:
		print("You are a teenager!")

elif age_0 > 20 and age_0 < 65:
	print("You are an adult!")

elif age_0 > 65:
	print("You are an elder!")
